    <td valign="top" class="word1"><p><img border="0" alt="" align="right" style="width: 184px; height: 272px;" src="/Public/images/2012514033342603.jpg"/></p><p dir="ltr" style="line-height: 2em; margin-bottom: 5px;"><span style="font-size:18px">&nbsp; &nbsp;&nbsp;</span><span style="font-size: 16px;">在中国经济和社会发展的新版图上，文化产业正如一轮朝阳，冉冉升起。文化产业的发展，适应了中国公众日益丰富的精神文化需求，带动了产业结构的调整和升级转型，并成为新常态下提升国家文化软实力、对外传播中国形象的重要载体。<br/>&nbsp; &nbsp; 在中国文化产业快速发展的进程中，深圳，这个以三十年改革开放奇迹而闻名的新兴城市，像一匹疾奔的骏马，奋然前行。深圳，不仅创造了文化创意产业增加值占GDP总值近10%的奇迹，创造了“文化+科技”、“文化+旅游”、“文化+金融”、“文化+创意”等发展模式，创造了腾讯、华强、雅昌、雅图、A8等一批高科技企业为代表的文化产业新型业态，更创造了文博会这一国家级、国际性、市场化的文化产业展示与交易平台。在深圳实现“文化自觉”的过程中，文化产业已经并将继续发挥重要作用。<br/>&nbsp; &nbsp; 身处深圳这块市场经济沃土的深圳大学，向以“特区大学、窗口大学、实验大学”为特色，以改革创新为己任，以服务特区为尊荣。在文化产业成为深圳第四大支柱产业、文博会举办经年成效显著的背景下，深圳大学审时度势，于2009年5月毅然成立文化产业研究院。多年来，在社会各界的关心下，研究院秉承“深化文化产业研究，服务经济社会发展，构建学术交流平台，打造新型特色智库”的宗旨，紧密结合建设“社会主义文化强国”、大力发展文化产业的战略规划，创新管理机制，整合科研力量，坚持学术影响与社会效益并重，在学术研究、决策咨询、社会服务、国际交流、项目开发、校企合作等方面取得系列成果，与北京大学文化产业研究院、中国传媒大学文化发展研究院、欧盟KEA创意组织、澳大利亚科廷大学等开展战略合作，得到“中央财政支持地方高校发展专项资金”资助，成为“广东省普通高校人文社会科学重点研究基地”、“深圳市文化创意产业研究基地”，获得国家社科基金重大项目；2013年，经文化部同意筹建“国家文化创新研究中心”；致力打造的“文化科技创新论坛”（CTIS）、文化科技蓝皮书等学术品牌的影响力也在日渐扩大。<br/>&nbsp; &nbsp; 雄关漫道，从头而越。深圳大学文化产业研究院将紧密结合深圳市“文化立市”、大力发展文化创意产业的战略规划，注重学术影响与社会效益并重，努力形成立足深圳、服务粤港澳台、辐射全国的文化产业学术研究中心、资料信息中心和人才培养中心，进一步提升深圳大学在华南地区乃至全国文化产业研究领域的地位。我们期望实施高效服务，助推中国文化产业健康快速发展；期望强化品牌建设，形成明确的定位与特色；期望通过学术研究与产业实操的有效结合，成为文科“产学研”示范基地；期望借助运行机制创新，为科研机构管理和体制改革积累有益经验；期望在文化传递与价值重塑的关键时期，做这个伟大时代文化创新的“沉思者”和“建言人”。</span></p><p dir="ltr" style="line-height: 2em; margin-bottom: 5px;"><span style="font-size: 16px;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</span><img alt="" src="/Public/images/20125141332343036.jpg"/></p><p dir="ltr" style="line-height: 2em; margin-bottom: 5px;"><span style="font-size: 16px;">&nbsp; &nbsp; （深圳大学副校长、文化产业研究院院长、国家文化创新研究中心（筹）主任）</span><br/></p><p style="line-height: 2em; margin-bottom: 5px;"><span style="font-size: 18px;">&nbsp;</span></p><