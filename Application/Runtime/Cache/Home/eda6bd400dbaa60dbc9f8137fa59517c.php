    <td valign="top" class="word1"><p style="TEXT-ALIGN: center"><img style="WIDTH: 279px; HEIGHT: 395px" title="1&amp;#26446;&amp;#20964;&amp;#20142;&amp;#29031;&amp;#29255;.JPG" src="http://ici.szu.edu.cn/ueditor/asp/upload/20160518/201605181030362138013.JPG" width="444" height="596"/></p><p><br/></p><p style="text-indent: 0em;"><br/></p><p style="text-indent: 2em;">李凤亮，男，1971年12月生于江苏阜宁。江苏师范大学（原徐州师范大学）本科毕业（1994），暨南大学文学博士（2001），中山大学博士后（2001-2004），美国南加州大学（USC）访问学者（2007-2008），“百千万人才工程”国家级人选及“有突出贡献中青年专家”（2015），国家社会科学基金重大项目“文化与科技融合创新的内在机理与战略路径研究”首席专家（2011），教育部艺术学理论类专业教学指导委员会委员（2013），国家社会科学基金艺术学项目评审专家（2013-2014），“国家百千万人才工程”入选者（2015）并被授予“有突出贡献中青年专家”荣誉称号，<span style="text-indent: 2em;">教育部“新世纪优秀人才支持计划”入选者（2007），霍英东教育基金会“高校青年教师基金”（2006）和“高校青年教师奖”（2008）获得者，广东省高校“千百十工程”省级学术带头人培养对象（2008），广东省委宣传部及省文联“新世纪之星”入选者（2008），深圳市第三届“鹏城杰出人才奖”获得者（2015），深圳市国家级高层次专业领军人才（2012），深圳大学领军学者（2013）。</span></p><p style="text-indent: 2em;">先后任教于江苏师范大学、暨南大学，曾为暨南大学最年轻的文科教授、博士生导师。2008年入选“广东省双百领导干部人才计划”，调任深圳大学党委常委、副校长，“制度创新与文化创意经济”二级学科博士点和“艺术学理论”一级学科硕士点带头人，兼任文化部国家文化创新研究中心主任、创新型城市建设与治理协同创新中心常务副主任、基础教育与教师发展研究中心主任、《深圳大学学报》（人文社科版）主编。为暨南大学文艺学专业博士生导师，中国文艺评论基地（暨南大学）副主任，广东省高校人文社科重点研究基地“海外华文文学与华语传媒研究中心”学术委员会委员、研究员，《东亚人文》杂志编委。</p><p style="text-indent: 2em;">2009年创办并兼任深圳大学文化产业研究院院长。通过凝聚校内外研究力量，积极承担各类纵横向课题，在学术研究、决策咨询、社会服务、国际交流、项目开发、校企合作等方面取得系列成果，先后成为“深圳市文化创意产业研究基地”、“广东省普通高校人文社科重点研究基地”，获得“中央财政支持地方高校发展专项资金”资助和“国家社会科学基金重大项目”立项。研究院注重学术影响与社会效益并重，努力创建立足深圳、服务广东、辐射全国的文化产业学术研究中心、资料信息中心和人才培养中心。2013年11月，获文化部批准筹建国家文化创新研究中心。</p><p style="text-indent: 2em;">担任的学术兼职有：中国世界华文文学学会副会长，海峡两岸文化创意产业高校研究联盟副理事长，中国外国文论与比较诗学研究会副会长，中国中外文艺理论学会常务理事兼文化创意产业研究会常务副会长，中国文艺理论学会副秘书长，全国马列文论研究会理事，文化产业（中国）协作体专家委员会委员，中国服务贸易协会专家委员会副主任委员，甘肃省委宣传部华夏文明传承创新研究专家组成员，广东省青年社会科学工作者协会副会长，广东省中国文学学会副会长，广东省文艺评论家协会副主席，广东省青年联合会常委，深圳市设计联合会副会长，深圳市设计与艺术联盟副主席，深圳市青年科技人才协会常务副会长，深圳市青年联合会常委，市社科联主席团成员，市文联委员，深圳市当代艺术与城市规划馆理事会成员，深圳市博物馆理事会成员，深圳市、福田区文化创意行业协会顾问。主持制定区域和园区文化创意产业规划多部，多次应邀担任国家社科基金、文化部文化科技创新项目、教育部和广东省人文社科项目、省市文化产业及宣传文化基金项目评审专家。</p><p style="text-indent: 2em;">二十余年来致力于文艺理论批评与文化研究工作，研究专长在文艺理论、文化创意产业与城市文化研究，其中米兰·昆德拉小说诗学研究、当代海外华人学者批评理论研究、文化与科技融合研究具有开拓性贡献。独立主持国家级课题3项，省部级课题8项，承担深圳市委市政府年度重大调研课题“深港共建全球性文化创意中心”、“深圳城市发展中的文化自觉”等。出版著作13部，发表学术论文100余篇，在《人民日报》等报刊发表文化评论近200篇。致力于推动教育改革和现代大学制度建设，有关高等教育改革、文化产业发展、社会科学管理的相关观点被《中国青年报》、《中国社会科学报》、《中华读书报》、《南方都市报》等以专版进行报道。曾获得霍英东教育基金会第十一届“高校青年教师奖”、广东省哲学社会科学优秀成果奖、广东省文学评论奖、广东省高校“千百十工程”第五批培养对象先进个人、鹏城杰出人才奖等学术科研奖励，并入选广东省宣传思想战线优秀人才“十百千工程”培养对象。</p><p style="text-indent: 0em;"><br/></p></td>
